package ch02.sec02;

public class IntegerLiteralExample {
	public static void main(String[] args) {
		int var1 = 0b1011;
		int var2 = 0206;
		int var3 = 365;
		int var4 = 0xB3;
		System.out.printf("var1: %d\n", var1);
		System.out.printf("var2: %d\n", var2);
		System.out.printf("var3: %d\n", var3);
		System.out.printf("var4: %d\n", var4);
	}
}

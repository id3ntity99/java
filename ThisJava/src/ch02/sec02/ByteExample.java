package ch02.sec02;

public class ByteExample {
	public static void main(String[] args) {
		byte var1 = -128;
		byte var2 = -30;
		byte var3 = 0;
		byte var4 = 30;
		byte var5 = 127;
		//byte var6 = 128; This line will raise Exception.
		System.out.printf("var1: %d\n", var1);
		System.out.printf("var2: %d\n", var2);
		System.out.printf("var3: %d\n", var3);
		System.out.printf("var4: %d\n", var4);
		System.out.printf("var5: %d\n", var5);
	}
}
